# 대입 연산자
# 변수는 최초에 어떤값이든 넣는게 좋습니다: 변수 초기화: 0, "" 등
a=2
print(a)

# 숫자 더한 후 저장: b=b+2
b=2
b+=2
print(b)

# 숫자 뺀 후 저장: c=c-2
c=2
c-=2
print(c)

# 숫자 곱한 후 저장: d=d*2
d=2
d*=2
print(d)
# 숫자 나눈 후 저장: e=e/2
e=2
e/=2
print(e)
# 숫자 나눈 몫을 저장: f=f//2
f=2
f//=2
print(f)
# 숫자 나눈 나머지를 저장: g=g%2
g=2
g%=2
print(g)
# 숫자를 거듭 제곱후 저장: h=h**2
h=2
h**=2
print(h)
